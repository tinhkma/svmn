    private boolean validateText(JTextField text) {
        if (text.getText().equals("")) {
            lb_noti.setVisible(true);
            lb_noti.setText("Vui long dien day du thong tin!");
            return false;
        }
        return true;
    }
    
    private boolean validateFloat(JTextField text) {
        try {
            float i = Float.parseFloat(text.getText());
            return true;
        } catch (NumberFormatException e) {
            lb_noti.setVisible(true);
            lb_noti.setText("Phai la so!");
            return false;
        }
    }
