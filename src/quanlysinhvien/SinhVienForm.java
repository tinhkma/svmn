    private void jb_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_addActionPerformed
        // TODO add your handling code here:
        siSoDefault = siSoDefault;
        if (
                validateText(jt_diemQt) 
                && validateText(jt_mssv) 
                && validateFloat(jt_diemHp) 
                && validateFloat(jt_diemQt)
            ) 
        {
            if (siSoCurrent > siSoDefault) {
                lb_noti.setVisible(true);
                lb_noti.setText("Vuot qua si so!");
                return;
            } else {
                siSoCurrent++;
            }
            sinhViens = new ArrayList<>();
            sinhViens.add(new SinhVien(
                jt_hoTen.getText(), 
                jt_mssv.getText(), 
                Float.parseFloat(jt_diemHp.getText()), 
                Float.parseFloat(jt_diemQt.getText())));
            if (siSoCurrent == siSoDefault) {
                lopHoc = new LopHoc(sinhViens, siSoCurrent, tenLop);
                this.setVisible(false);
                ViewLopForm viewLopForm = new ViewLopForm(lopHoc);
                viewLopForm.setVisible(true);
            }
        }
    }//GEN-LAST:event_jb_addActionPerformed

